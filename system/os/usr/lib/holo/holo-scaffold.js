// _shared/holo-scaffold.js — the Holo SDK scaffolder (ADR-0050), ISOMORPHIC + pure (no Node, no fs):
// given { id, name, summary, category, accent, minimal } it returns the bytes of a fully functional,
// beautiful, serverless, content-addressed holospace — its index.html (auto-wired to the five OS core
// modules, QVAC-style @hologram/sdk imports, embedded schema.org JSON-LD conformance), holospace.json
// manifest, and icon.svg. ONE source of templates, two front ends: the CLI (tools/create-holospace.mjs)
// writes the files + drives the build pipeline; the browser playground (apps/sdk) calls scaffold() live.

export const pascal = (s) => String(s).replace(/[^a-z0-9]+/gi, " ").trim().split(/\s+/).map((w) => w[0].toUpperCase() + w.slice(1)).join("");

// scaffold(opts) → { id, identifier, name, summary, category, accent, minimal, manifest, files }
// files = { "holospace.json": string, "index.html": string, "icon.svg": string } — ready to write.
export function scaffold(opts = {}) {
  let { id, name, summary, category, accent, minimal } = opts;
  if (!name) name = "Holo " + pascal(id);
  const identifier = "org.hologram." + (pascal(name).startsWith("Holo") ? pascal(name) : "Holo" + pascal(name));
  if (!summary) summary = `${name} — a serverless, content-addressed holospace, auto-wired to Holo UI · UX · Terms · Privacy · Conform.`;
  if (!category) category = "Utility";
  if (!accent) accent = "";
  minimal = !!minimal;

  // The manifest. `shared` seeds holo-sdk.js (imported via the "@hologram/sdk" map, so it carries no
  // path ref for gen-imports to discover); the rest gen-imports RECONCILES from the entry's refs.
  // conforms.specs name only ids that EXIST (the gate rejects unknown ids). holo-conscience.js is
  // pinned into the closure automatically by build-app (ADR-033) — constitution-bound for free.
  const manifest = {
    $comment: "Generated by tools/create-holospace.mjs (Holo SDK, ADR-0050). Edit freely; re-run `gen-imports` + `build-app` after changing shared refs.",
    id: identifier,
    name,
    type: ["schema:SoftwareApplication", "schema:WebApplication"],
    summary,
    entry: "index.html",
    icon: "icon.svg",
    applicationCategory: category,
    // Built ON the Holo Product foundation (ADR-0065): it inherits the balanced Holo UI ⊕ Holo UX
    // faculties (via the wired engines below) and follows the hybrid method (see DECISION.md).
    builtOn: "holo-product",
    conforms: { specs: ["holo-sdk", "holo-terms", "holo-privacy", "holo-conform", "did-core", "json-ld", "sri", "wcag"] },
    capabilities: { storage: [identifier] },
    shared: ["holo-sdk.js"],
    engines: [],
  };

  // A simple themed glyph — currentColor so it inherits the accent.
  const iconSvg = `<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 48 48" fill="none" stroke="currentColor" stroke-width="3" stroke-linecap="round" stroke-linejoin="round" color="${accent || "#5b8cff"}">
  <circle cx="24" cy="24" r="18" opacity="0.35" />
  <path d="M24 8 L38 16 V32 L24 40 L10 32 V16 Z" />
  <circle cx="24" cy="24" r="4" fill="currentColor" stroke="none" />
</svg>
`;

  // The five core modules, as data-holo-shared siblings. Classic vs module per each file's form.
  const CORE_SCRIPTS = [
    `<script src="../../_shared/holo-ui-kernel.js"></script>`,
    `<script type="module" src="../../_shared/holo-ux.js"></script>`,
    `<script src="../../_shared/holo-terms.js"></script>`,
    `<script src="../../_shared/holo-privacy.js"></script>`,
    `<script type="module" src="../../_shared/holo-conscience.js"></script>`,
    `<script type="module" src="../../_shared/holo-app.mjs"></script>`,
  ];
  const EXTRA_SCRIPTS = [
    `<script src="../../_shared/holo-icons.js"></script>`,
    `<script type="module" src="../../_shared/holo-object.js"></script>`,
  ];
  // holo-sdk.js is NOT a <script> tag — it is imported via the "@hologram/sdk" map below (QVAC-style).
  const scripts = [...CORE_SCRIPTS, ...(minimal ? [] : EXTRA_SCRIPTS)].join("\n    ");

  // The page is itself a W3C semantic-web document: schema.org SoftwareApplication as JSON-LD that
  // DECLARES (dcterms:conformsTo) the open standards + the holospaces spec it answers to.
  const ld = JSON.stringify({
    "@context": { schema: "https://schema.org/", dcterms: "http://purl.org/dc/terms/" },
    "@type": ["schema:SoftwareApplication", "schema:WebApplication"],
    "schema:name": name,
    "schema:description": summary,
    "schema:applicationCategory": category,
    "schema:identifier": identifier,
    "schema:isAccessibleForFree": true,
    "schema:operatingSystem": "Hologram OS — UOR content-addressable substrate (serverless; boots by content address)",
    "dcterms:conformsTo": [
      { "@id": "https://www.w3.org/TR/json-ld11/" },
      { "@id": "https://www.w3.org/TR/did-core/" },
      { "@id": "https://www.w3.org/TR/SRI/" },
      { "@id": "https://www.w3.org/TR/rdf-canon/" },
      { "@id": "https://www.w3.org/TR/WCAG22/" },
      { "@id": "https://github.com/Hologram-Technologies/holospaces" },
      { "@id": "https://hologram.os/ns/product" },
    ],
  });

  const indexHtml = `<!doctype html>
<html lang="en">
  <head>
    <meta charset="utf-8" />
    <meta name="viewport" content="width=device-width, initial-scale=1, viewport-fit=cover" />
    <title>${name}</title>
    <meta name="description" content="${summary.replace(/"/g, "&quot;")}" />
    <link rel="icon" href="icon.svg" />
    <link rel="stylesheet" href="../../_shared/holo-theme.css" />
    <link rel="stylesheet" href="../../_shared/holo-mobile.css" />
    <link rel="stylesheet" href="../../_shared/holo-phi.css" />
    <!-- the SDK is imported by the familiar QVAC-style specifier; gen-imports pins this map to
         holo-sdk.js's content address (κ), so "@hologram/sdk" resolves by content (Law L1), no npm. -->
    <script type="importmap">{ "imports": { "@hologram/sdk": "../../_shared/holo-sdk.js", "@hologram/app": "../../_shared/holo-app.mjs" } }</script>
    <!-- W3C semantic-web: the app describes itself (schema.org) + declares its conformance as JSON-LD. -->
    <script type="application/ld+json">${ld}</script>
    <style>
      :root { color-scheme: light dark; --accent: var(--holo-accent, #5b8cff); }
      * { box-sizing: border-box; }
      html, body { height: 100%; margin: 0; }
      body { background: var(--holo-bg); color: var(--holo-ink); display: flex; align-items: center; justify-content: center;
        font: var(--holo-text-sm, 1rem)/1.5 var(--holo-font-sans, ui-sans-serif, system-ui, -apple-system, "Segoe UI", Roboto, sans-serif);
        padding: max(var(--holo-size-m, 1.618rem), var(--safe-t, 0px)) var(--holo-size-m, 1.618rem); }
      .card { width: min(38rem, 100%); background: var(--holo-surface); border: 1px solid var(--holo-border);
        border-radius: var(--holo-radius, 16px); padding: var(--holo-size-l, 2.618rem); }
      .brand { display: flex; align-items: center; gap: .7rem; }
      .brand .logo { color: var(--accent); display: inline-flex; }
      h1 { font-size: var(--holo-size-m, 1.618rem); margin: 0; letter-spacing: -0.01em; }
      p.sub { color: var(--holo-ink-dim); margin: .3rem 0 0; }
      .grid { display: grid; grid-template-columns: repeat(auto-fit, minmax(8.5rem, 1fr)); gap: .55rem; margin: var(--holo-size-m, 1.618rem) 0; }
      .tile { display: flex; flex-direction: column; gap: .3rem; padding: .7rem .8rem; border: 1px solid var(--holo-border);
        border-radius: 12px; background: var(--holo-surface-2); }
      .tile .k { font: var(--holo-text-sm, 1rem) ui-monospace, monospace; color: var(--holo-ink-dim); text-transform: uppercase; letter-spacing: .06em; }
      .tile .v { display: inline-flex; align-items: center; gap: .4rem; font-weight: 600; }
      .dot { width: .55rem; height: .55rem; border-radius: 50%; background: var(--holo-ink-dim); flex: 0 0 auto; }
      .dot.ok { background: var(--holo-ok, #3fb950); } .dot.bad { background: var(--holo-danger, #f85149); }
      .actions { display: flex; flex-wrap: wrap; gap: .55rem; }
      button { font: inherit; font-weight: 600; min-height: 44px; padding: 0 1rem; cursor: pointer; color: var(--holo-ink);
        background: var(--holo-surface-2); border: 1px solid var(--holo-border); border-radius: 12px;
        display: inline-flex; align-items: center; gap: .45rem; }
      button:hover { border-color: color-mix(in srgb, var(--accent) 50%, var(--holo-border)); }
      button:focus-visible { outline: var(--holo-focus-width, 3px) solid color-mix(in srgb, var(--accent) 60%, transparent); outline-offset: 2px; }
      button.primary { background: color-mix(in srgb, var(--accent) 16%, var(--holo-surface-2)); border-color: color-mix(in srgb, var(--accent) 40%, var(--holo-border)); }
      #out { margin-top: .8rem; min-height: 1.4em; color: var(--holo-ink-dim); font: var(--holo-text-sm, 1rem) ui-monospace, monospace; white-space: pre-wrap; }
      footer { margin-top: var(--holo-size-m, 1.618rem); display: flex; align-items: center; gap: .5rem; color: var(--holo-ink-dim); font: var(--holo-text-sm, 1rem) ui-monospace, monospace; }
      footer .k { overflow: hidden; text-overflow: ellipsis; white-space: nowrap; }
    </style>
  </head>
  <body>
    <main class="card">
      <header class="brand">
        <span class="logo"><holo-icon name="hub" size="30" aria-hidden="true"></holo-icon></span>
        <div><h1>${name}</h1><p class="sub">${summary.replace(/</g, "&lt;")}</p></div>
      </header>
      <section class="grid" id="wiring" aria-label="Core module wiring"></section>
      <div class="actions">
        <button class="primary" id="accent"><holo-icon name="palette" size="18" aria-hidden="true"></holo-icon> Cycle accent</button>
        <button id="tier"><holo-icon name="tune" size="18" aria-hidden="true"></holo-icon> Refresh tier</button>
        <button id="disclose"><holo-icon name="lock" size="18" aria-hidden="true"></holo-icon> Request disclosure</button>
        <button id="check"><holo-icon name="verified" size="18" aria-hidden="true"></holo-icon> Conscience check</button>
      </div>
      <div id="out" role="status" aria-live="polite"></div>
      <!-- build · run · share — the three native verbs, present in every holospace (ADR-0051). -->
      <section style="margin-top: var(--holo-size-m, 1.618rem)"><holo-app></holo-app></section>
      <footer><span class="dot" id="seal"></span><span class="k" id="did">verifying…</span><span class="k" id="foundation" style="margin-left:auto"></span></footer>
    </main>

    <!-- The five OS core modules + the Holo SDK façade, auto-wired by the Holo SDK generator
         (ADR-0050). gen-imports rewrites each ref to a /.holo/sha256/<κ> content address. -->
    ${scripts}
    <script type="module">
      // QVAC SDK format: flat, named, functional imports from one package + options-object args.
      import { ready, info, setAccent, refreshTier, disclose, evaluate, sealed, address, verify, on, product } from "@hologram/sdk";

      const APP_NAME = ${JSON.stringify(name)};
      const ACCENT = ${JSON.stringify(accent)};
      const PALETTE = ["#5b8cff", "#22c55e", "#f59e0b", "#ec4899", "#8b5cf6", "#06b6d4"];
      const MODS = [
        ["UI", "ui"], ["UX", "ux"], ["Terms", "terms"],
        ["Privacy", "privacy"], ["Conform", "conform"], ["Object", "object"],
      ];
      const $ = (s) => document.querySelector(s);
      const say = (m) => { $("#out").textContent = m; };

      function render() {
        const i = info();
        const grid = $("#wiring");
        grid.innerHTML = "";
        for (const pair of MODS) {
          const label = pair[0], key = pair[1];
          const onv = !!i[key];
          const tile = document.createElement("div");
          tile.className = "tile";
          const v = (key === "ux" && i.tier) ? i.tier : (onv ? "wired" : "—");
          tile.innerHTML = '<span class="k">Holo ' + label + '</span>'
            + '<span class="v"><span class="dot ' + (onv ? "ok" : "bad") + '"></span>' + v + '</span>';
          grid.appendChild(tile);
        }
      }

      await ready();
      if (ACCENT) { try { setAccent(ACCENT); } catch (e) {} }
      render();
      on("holo-ui-change", render);
      on("holo-ux-change", render);

      // built ON the Holo Product foundation (ADR-0065) — read it live from the SDK (faculties +
      // method + balance), so the app declares the foundation it inherits rather than re-deciding it.
      try { const f = await product(); if (f) $("#foundation").textContent = "built on Holo Product" + (f.balanced ? " · UI⊕UX balanced" : ""); } catch (e) {}

      // self-verify live (Law L5): address a small object and re-derive it.
      try {
        const obj = { "@type": "schema:SoftwareApplication", "schema:name": APP_NAME };
        const did = await address(obj);
        const ok = await verify({ id: did, ...obj });
        $("#seal").className = "dot " + (ok ? "ok" : "bad");
        $("#did").textContent = did;
        $("#did").title = did;
      } catch (e) { $("#did").textContent = "HoloObject not wired (run with extras)"; }

      let pi = 0;
      $("#accent").addEventListener("click", () => { pi = (pi + 1) % PALETTE.length; setAccent(PALETTE[pi]); say("accent → " + PALETTE[pi]); });
      $("#tier").addEventListener("click", () => { const s = refreshTier(); render(); say("device tier → " + (s && s.tier)); });
      $("#disclose").addEventListener("click", async () => {
        const vp = await disclose({ purpose: "dpv:ServiceProvision", recipient: APP_NAME, claims: [{ category: "dpv-pd:EmailAddress", name: "email" }] });
        if (!vp) say("disclosure → default-deny (no standing grant) — privacy holds by construction");
        else say("disclosure → Verifiable Presentation, " + vp.level.disclosed + " of " + vp.level.total + " details");
      });
      $("#check").addEventListener("click", () => {
        const r = evaluate({});
        say("conscience → " + r.outcome + (sealed() ? " (constitution sealed, Law L5)" : " (fail-closed: unsealed)"));
      });
    </script>
  </body>
</html>
`;

  // DECISION.md — the method scaffold (Holo Product, ADR-0065). A product is a recorded decision:
  // this seeds the hybrid lifecycle so a new holospace starts with Discover · Define · Verify in place
  // (the rest — Design · Build · Deliver — are inherited from the foundation + the build pipeline).
  const decisionMd = `# Decision — ${name}

> The decision record for this holospace, on the Holo Product foundation (ADR-0065). An ADR is the
> **Define** phase, the value statement is **Discover**, the conformance gate is **Verify** — a
> product is a recorded decision, designed to both doctrines, and proven before it ships.

## Discover — the value
${summary}

_Who is it for, and what does it give them? Lead with why, then how, then what (the plain voice)._

## Define — the decision
- **Why:** _why this holospace exists._
- **How:** built on Holo Product — it inherits Holo UI (the look) ⊕ Holo UX (the experience),
  auto-wired to the five core modules; no UX/UI basics are re-decided here (Law L2).
- **What:** _what it does._

## Design — the faculties (inherited, balanced)
- **UI:** visual design · colours · graphic design · layouts · typography → the \`--holo-*\` tokens.
- **UX:** interaction · wireframes · information architecture · user research · scenarios → the Holo UX doctrine.

## Build · Verify · Deliver
- **Build:** a content-addressed κ-object — re-derivable, serverless (Law L5).
- **Verify:** conforms to the gate's app rows (\`#app-ui-*\` · \`#app-ux-*\`) — done is proven, not asserted.
- **Deliver & Iterate:** shared as \`holo://κ\`; evolve continuously without breaking the gate.
`;

  return { id, identifier, name, summary, category, accent, minimal, manifest,
    files: { "holospace.json": JSON.stringify(manifest, null, 2) + "\n", "index.html": indexHtml, "icon.svg": iconSvg, "DECISION.md": decisionMd } };
}

export default scaffold;
