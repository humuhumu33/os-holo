// Generate the machine-readable reference FROM the real source of truth, so the
// docs cannot silently drift from the implementation (the prompt's Step 7).
//
//   conformance.jsonld  → reference/conformance.mdx  +  public/reference/conformance.json
//   docs/adr/*.md        → architecture/adrs.mdx       +  public/reference/adrs.json
//   the whole site       → public/llms.txt            (an agent's index to these docs)
//
// Run before every build (`npm run gen`, wired into `prebuild`). In CI the build
// regenerates these, so what ships always matches the source; `--check` fails when
// the committed output is stale (a documented contract diverged from the code).

import { readFileSync, writeFileSync, readdirSync, mkdirSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, resolve } from 'node:path';

const HERE = dirname(fileURLToPath(import.meta.url));
const R = (p) => resolve(HERE, p);
const REPO = 'https://github.com/humuhumu33/os-holo';
const ADR_SRC = (f) => `${REPO}/blob/main/system/docs/adr/${f}`;

const check = process.argv.includes('--check');
// A JSX comment placed in the MDX body (after the frontmatter, which must be first).
const GENERATED = (src) =>
  `{/* GENERATED by system/docs/site/scripts/gen-reference.mjs from ${src}.\n    Do not edit by hand — it is overwritten on the next build. Fix the source, then \`npm run gen\`. */}\n\n`;

// ── helpers ────────────────────────────────────────────────────────────────
const firstSentence = (s, cap = 240) => {
  const t = String(s || '').replace(/\s+/g, ' ').trim();
  const dot = t.search(/[.:](\s|$)/);
  const head = dot > 40 ? t.slice(0, dot) : t;
  return head.length > cap ? head.slice(0, cap - 1).trimEnd() + '…' : head;
};
const esc = (s) => String(s).replace(/\|/g, '\\|').replace(/</g, '&lt;');

// ── conformance.jsonld → the gate reference ──────────────────────────────────
function conformance() {
  const cat = JSON.parse(readFileSync(R('../../../os/etc/conformance.jsonld'), 'utf8'));
  const rows = (cat.conforms || []).map((r) => ({
    id: String(r['@id'] || '').split('#')[1] || r['@id'],
    title: firstSentence(r.name, 160),
    category: r['hosc:category'] || 'Other',
    witness: r['hosc:witness'] || null,
    required: r['hosc:required'] === true,
    authority: r['hosc:authority'] || null,
  }));
  rows.sort((a, b) => a.category.localeCompare(b.category) || a.id.localeCompare(b.id));

  const byCat = new Map();
  for (const r of rows) (byCat.get(r.category) || byCat.set(r.category, []).get(r.category)).push(r);

  let md = `---\ntitle: Conformance rows\ndescription: Every required claim the release gate proves, joined from the conformance catalog. ${rows.length} rows; the gate fails closed if any required row is unwitnessed.\naudience: [developers, agents]\n---\n\n`;
  md += GENERATED('system/os/etc/conformance.jsonld');
  md += `import { Aside } from '@astrojs/starlight/components';\n\n`;
  md += `A **conformance row** is one claim about the OS, paired with the **witness** that proves it. `;
  md += `\`node tools/gate.mjs\` re-runs every witness, joins the results to this catalog, and exits non-zero if any **required** row is unproven — so a non-conforming build cannot ship.\n\n`;
  md += `<Aside type="note">This page is generated from [\`system/os/etc/conformance.jsonld\`](${REPO}/blob/main/system/os/etc/conformance.jsonld). ${rows.length} rows · ${rows.filter((r) => r.required).length} required.</Aside>\n\n`;
  for (const c of [...byCat.keys()].sort()) {
    md += `## ${c}\n\n| Row | Proven by | Required |\n| --- | --- | --- |\n`;
    for (const r of byCat.get(c)) {
      const w = r.witness ? `[\`${r.witness.replace('tools/', '')}\`](${REPO}/blob/main/system/${r.witness})` : '—';
      md += `| **${esc(r.id)}** — ${esc(r.title)} | ${w} | ${r.required ? '✓' : ' '} |\n`;
    }
    md += `\n`;
  }
  writeJSON('../public/reference/conformance.json', { source: 'system/os/etc/conformance.jsonld', count: rows.length, rows });
  return { mdx: ['../src/content/docs/reference/conformance.mdx', md], count: rows.length };
}

// ── docs/adr/*.md → the architecture index ───────────────────────────────────
function adrs() {
  const dir = R('../../adr');
  const files = readdirSync(dir).filter((f) => /^\d{4}-.*\.md$/.test(f)).sort();
  const list = files.map((f) => {
    const txt = readFileSync(resolve(dir, f), 'utf8');
    const h = txt.match(/^#\s*ADR-?(\d+)\s*[:–-]\s*(.+)$/m);
    const st = txt.match(/\*\*Status:\*\*\s*([^.\n]+)/);
    const num = h ? h[1] : f.slice(0, 4);
    const titleFull = h ? h[2].trim() : f;
    return {
      num,
      slug: f.replace(/\.md$/, ''),
      title: firstSentence(titleFull.split('—')[0].split(':')[0], 90),
      full: titleFull,
      status: st ? st[1].trim() : 'Accepted',
      href: ADR_SRC(f),
    };
  });

  let md = `---\ntitle: Architecture decisions\ndescription: Every architecture decision record (ADR) — the durable why behind each design choice. ${list.length} records, linked to source.\naudience: [developers, agents]\n---\n\n`;
  md += GENERATED('system/docs/adr/*.md');
  md += `import { Aside } from '@astrojs/starlight/components';\n\n`;
  md += `An **ADR** records one decision: the problem, the choice, and what it costs. They are the primary sources for everything in these docs — when a page says *why*, an ADR says it first and in full.\n\n`;
  md += `<Aside type="tip">Read **0025** (everything is a self-verifying object), **0026** (the OS serves itself by hash), and **0024** (correctness is witnessed, not claimed) before the rest.</Aside>\n\n`;
  md += `Generated from [\`system/docs/adr/\`](${REPO}/tree/main/system/docs/adr). ${list.length} records.\n\n`;
  md += `| ADR | Decision | Status |\n| --- | --- | --- |\n`;
  for (const a of list) {
    md += `| [${a.num}](${a.href}) | ${esc(a.title)} | ${esc(a.status.replace(/\s+/g, ' ').slice(0, 40))} |\n`;
  }
  md += `\n`;
  writeJSON('../public/reference/adrs.json', { source: 'system/docs/adr/', count: list.length, adrs: list });
  return { mdx: ['../src/content/docs/architecture/adrs.mdx', md], count: list.length };
}

