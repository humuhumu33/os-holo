// ── write / check ────────────────────────────────────────────────────────────
function writeJSON(rel, obj) {
  const p = R(rel);
  mkdirSync(dirname(p), { recursive: true });
  emit(p, JSON.stringify(obj, null, 2) + '\n');
}
const drift = [];
function emit(absOrRel, content) {
  const p = absOrRel.startsWith('/') || /^[A-Za-z]:/.test(absOrRel) ? absOrRel : R(absOrRel);
  mkdirSync(dirname(p), { recursive: true });
  let prev = '';
  try { prev = readFileSync(p, 'utf8'); } catch {}
  if (prev === content) return;
  if (check) { drift.push(p); return; }
  writeFileSync(p, content);
}

const conf = conformance();
const adr = adrs();
emit(...conf.mdx);
emit(...adr.mdx);
emit(...llms(conf.count, adr.count));

if (check && drift.length) {
  console.error('Reference is STALE — regenerate with `npm run gen`:');
  for (const f of drift) console.error('  drift:', f);
  process.exit(1);
}
console.log(`Reference generated: ${conf.count} conformance rows, ${adr.count} ADRs.`);
